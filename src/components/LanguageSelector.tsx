const LanguageSelector = () => {
  const { language, setLanguage } = useLanguage();
  const [open, setOpen] = useState(false);

  const handleLanguageChange = (code: string) => {
    setLanguage(code);
    setOpen(false);
  };

  const selectedLanguage = languageOptions.find(option => option.code === language) || languageOptions[0];

  return (
    <DropdownMenu open={open} onOpenChange={setOpen}>
      <DropdownMenuTrigger asChild>
        <Button variant="outline" size="sm" className="flex items-center gap-1 h-8">
          <Globe className="h-4 w-4" />
          <span className="hidden md:inline">{selectedLanguage.name}</span>
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent className="w-56">
        <DropdownMenuLabel>Select Language</DropdownMenuLabel>
        <DropdownMenuSeparator />
        <div className="max-h-[300px] overflow-y-auto">
          <DropdownMenuGroup>
            {languageOptions.map((option) => (
              <DropdownMenuItem
                key={option.code}
                className="cursor-pointer"
                onClick={() => handleLanguageChange(option.code)}
              >
                <div className="flex items-center justify-between w-full">
                  <span>
                    {option.name} <span className="text-muted-foreground">({option.nativeName})</span>
                  </span>
                  {option.code === language && (
                    <Check className="h-4 w-4 text-primary" />
                  )}
                </div>
              </DropdownMenuItem>
            ))}
          </DropdownMenuGroup>
        </div>
      </DropdownMenuContent>
    </DropdownMenu>
  );
};

export default LanguageSelector;
